package org.bfg.generate;

public final class CharInfo {

    public final int x;
    public final int y;
    public final int width;
    public final int height;
    public final int ascent;

    public CharInfo(int x, int y, int width, int height, int ascent) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.ascent = ascent;
    }
}
